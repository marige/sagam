    public String codeImmo="1";
     String reqpart="select idIMMOBILISATIONMATERIEL as N0 ,LIBARTICLE as Immobilisation ,utilisateur,CODEMATERIEL as Immatriculation,libetat as Etat,libbur as BUREAU,DATACQ,budget from etat,immobilisationmateriel,article,categorie,bureau,utilisateur "
           + "where BUREAU.idbureau=IMMOBILISATIONMATERIEL.idbureau AND immobilisationmateriel.idarticle=article.idarticle and article.idcategorie=categorie.idcategorie and immobilisationmateriel.etat=etat.idetat"
           + " and immobilisationmateriel.idutilisateur=utilisateur.idutilisateur";    private boolean controle(){
        boolean ok=false;
        if(txtIdImmo.getText().isEmpty())
            JOptionPane.showMessageDialog(this,"Choisissez le materiel");
        else if(txtMontant.getText().isEmpty())
             JOptionPane.showMessageDialog(this,"saisissez le montant de l\'intervention");
        else if(!r.isInteger(txtMontant.getText()))
                 JOptionPane.showMessageDialog(this,"Le montant doit être un nombre");
        else if(txtDate.getDate()==null)
                 JOptionPane.showMessageDialog(this,"date de l\'intervention non saisie");
        else if(codePrestataire==null)
                 JOptionPane.showMessageDialog(this,"choisissez le prestataire");        
        else if(txtNatureIntervention.getText().isEmpty())
                 JOptionPane.showMessageDialog(this,"nature de l\'interventation est vide");      
        else
        ok=true;
        return ok;
    }
    
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
      
        if(controle()){
        int reponse= JOptionPane.showConfirmDialog(this,"voulez-vous enregistrez?","confirmation",JOptionPane.YES_NO_OPTION);
        if(reponse==JOptionPane.YES_OPTION){
            try {
                String valeur[]={codePrestataire,txtIdImmo.getText(),txtNatureIntervention.getText(),txtMontant.getText(),r.getDateChoisie(txtDate)};
                codePrestataire= alP.get(cmbPrestatataire.getSelectedIndex()).toString();
                
                int i=r.Insertion("REPARATION(idPRESTATAIRE,IDIMMOBILISATIONMATERIEL, LIBREPARATION, MONTANT, DATEREPARATION)",valeur);
                JOptionPane.showMessageDialog(this, i+" réparation engistrée");
                if(i==1){    
                    txtIdImmo.setText("");txtNatureIntervention.setText("");txtMontant.setText("");txtDate.setDate(null);
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this,ex.getMessage());
            }

        }
       }
    }//GEN-LAST:event_jButton2ActionPerformed

