    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        int reponse= JOptionPane.showConfirmDialog(this,"voulez-vous enregistrez?","confirmation",JOptionPane.YES_NO_OPTION);
        if(reponse==JOptionPane.YES_OPTION){
            try {
                String valeur[]={codeF.getText(),codeP.getText(),codeS.getText(),txtFamille.getText()};
                int i=f.Insertion("FAMILLE(idfamille,idCOMPTEP,IDGROUPESTOCK,LIBFAMILLE)",valeur);
                JOptionPane.showMessageDialog(this, i+" famille engistrée");
                if(i==1){
                    txtFamille.setText(null);
                    codeS.setText(null);
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this,ex.getMessage());
            }

        }
    }//GEN-LAST:event_jButton2ActionPerformed
   
