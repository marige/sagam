    private float calcMontant(String qte,String pu){
        float result=0;
        if(!(qte.isEmpty()||pu.isEmpty()))
          result=Float.parseFloat(qte)*Float.parseFloat(pu);       
       if(ckboxTva.getModel().isSelected()){
           result+=0.18*result;
       }
       return result;
    }
