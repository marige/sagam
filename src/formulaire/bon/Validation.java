    private void tableArticleMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableArticleMouseClicked
        idBon=tableArticle.getValueAt(tableArticle.getSelectedRow(),e.getColumnByName(tableArticle, "idBon")).toString();
        try {
       //     JOptionPane.showMessageDialog(this,idBon);
            e.getObjetEntree(idBon);
            txtRefFacture.setText(e.REFFACTURE);
            e.setAfficherDate(txtDateFacture, e.DATEFACTURE.toString());
            txtRefBordereau.setText(e.REFBORDEREAU);
            e.setAfficherDate(txtDateBordereau, e.DATEBORD.toString());
            d.afficherDetailBon(tableReception,Integer.parseInt(idBon));
            txtIdBon.setText(idBon);

        } catch (ParseException | SQLException ex) {
            JOptionPane.showMessageDialog(this,ex.getMessage());
        }
    }//GEN-LAST:event_tableArticleMouseClicked

