    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        int reponse= JOptionPane.showConfirmDialog(this,"voulez-vous enregistrez?","confirmation",JOptionPane.YES_NO_OPTION);
        if(reponse==JOptionPane.YES_OPTION){
            try {
                String valeur[]={txtcodeP.getText(),txtCompteprincipale.getText()};
                int i=cp.Insertion("COMPTEP(idcomptep,LIBCOMPTEP)",valeur);
                JOptionPane.showMessageDialog(this, i+" compte principale engistré");
                if(i==1){txtCompteprincipale.setText(null);
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this,ex.getMessage());
            }

        }
    }//GEN-LAST:event_jButton2ActionPerformed

