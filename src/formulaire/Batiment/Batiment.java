    private void MODSERVICEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MODSERVICEActionPerformed
        if(Tableservice.getSelectedRow()==-1){
         JOptionPane.showMessageDialog(this,"Choisissez une le Service √† Modifier");
        }else{
        service.idSERVICE=Integer.parseInt(Tableservice.getValueAt(Tableservice.getSelectedRow(),0).toString());
        service.LIBSERVICE=(Tableservice.getValueAt(Tableservice.getSelectedRow(), 1).toString());
        service.SERV=(Tableservice.getValueAt(Tableservice.getSelectedRow(),2).toString());
        service.CODESERVICE=(Tableservice.getValueAt(Tableservice.getSelectedRow(),3).toString());
        ModificationS Ms= new ModificationS(new JFrame(),true);
        Ms.Service=service;
        Ms.setVisible(true);
        }
    }//GEN-LAST:event_MODSERVICEActionPerformed

