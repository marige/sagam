    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

          if(libservice.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"saisissez le libellé du service");
        }
        else if(codeservice.getText().isEmpty())
            JOptionPane.showMessageDialog(this,"Saisissez le code servant à la codification");
        else if(serv.getText().isEmpty())
            JOptionPane.showMessageDialog(this,"saisissez le sigle");
        else{
        
        
        int reponse= JOptionPane.showConfirmDialog(this,"voulez-vous enregistrez?","confirmation",JOptionPane.YES_NO_OPTION);
        if(reponse==JOptionPane.YES_OPTION){
            try {
                String valeur[]={libservice.getText(),codeservice.getText(),serv.getText()};
                String champ[]={"LIBSERVICE","CODESERVICE","SERV"};
                //int i= service.updateTable("SERVICE",champ,valeur "where idSERVICE="+service.idSERVICE);
                int i=Service.updateTable("SERVICE", champ, valeur,"where idSERVICE="+Service.idSERVICE);
                codeservice.setText("");
                libservice.setText("");
                serv.setText("");
                JOptionPane.showMessageDialog(this, i+" service engistré");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this,ex.getMessage());
            }

        }
    }//GEN-LAST:event_jButton2ActionPerformed
    }
