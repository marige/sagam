    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        REPORT r=new REPORT();
        if(tableMaterielService.getModel().getRowCount()==0)
        JOptionPane.showMessageDialog(this,"aucune immo");
        else
        {
            try {
                //remplissage de hashMap
                HashMap hm= new HashMap();
                if(!CodeService.getText().isEmpty())
                hm.put("serviceutil", ComboSer.getSelectedItem().toString());
                
                if(!CodeBure.getText().isEmpty())
                hm.put("bureau", comboBureau.getSelectedItem().toString());
                hm.putAll(r.getInstitutionMap());
                r.reportWithJtable(tableMaterielService,hm,"ficheDetenteurMateriel");
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

