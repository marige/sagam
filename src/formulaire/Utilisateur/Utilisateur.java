    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       try{
        String idUtilisateur=table.getValueAt(table.getSelectedRow(),u.getColumnByName(table,"num")).toString();        
        if(u.isExist("immobilisationmateriel","idutilisateur",idUtilisateur))
            JOptionPane.showMessageDialog(this,"cet utilisateur est utlisé, suppression impossible");
        else{
             int reponse= JOptionPane.showConfirmDialog(this,"voulez-vous supprimer?","confirmation",JOptionPane.YES_NO_OPTION);
             if(reponse==JOptionPane.YES_OPTION){
                JOptionPane.showMessageDialog(this,u.insUpdateDel("delete from utilisateur where idutilisateur="+idUtilisateur)+" suppression réussie");
             }
           }
        } catch(IndexOutOfBoundsException ex){
          JOptionPane.showMessageDialog(this,"Choisissez utilisateur à supprimer,SVP");
        } catch(SQLException ex){
          JOptionPane.showMessageDialog(this,ex.getMessage());
        }
       Controle.modification=true;
    }//GEN-LAST:event_jButton1ActionPerformed

