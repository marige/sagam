    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        int reponse= JOptionPane.showConfirmDialog(this,"voulez-vous enregistrez?","confirmation",JOptionPane.YES_NO_OPTION);
        if(reponse==JOptionPane.YES_OPTION){
            try {
                String valeur[]={codeP.getText(),codeD.getText(),txtCompteD.getText()};
                int i=cd.Insertion("COMPTED(idCOMPTEP,idcompted,LIBCOMPTED)",valeur);
                JOptionPane.showMessageDialog(this, i+" compte divisionnaire engistré");
                if(i==1){
                    txtCompteD.setText(null);
                    codeD.setText(null);
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this,ex.getMessage());
            }

        }
    }//GEN-LAST:event_jButton2ActionPerformed

