    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(txtUtilisateur.getText().isEmpty())
        JOptionPane.showMessageDialog(this,"renseigner le nom utilisateur");
        else
        {
            try {
             int j=i.modification(idInventaire,i.getDateChoisie(txtDate),txtObservation.getText(),txtUtilisateur.getText());
             JOptionPane.showMessageDialog(this, j+" modification r√©ussie");
            } catch (SQLException ex) {
               JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        try {
            INVENTAIRE v=i.getObjetInventaire(idInventaire);           
            i.setAfficherDate(txtDate, v.DATEINVENT);
            txtUtilisateur.setText(v.UTILISATEUR);
            txtObservation.setText(v.OBSERVATION);
        } catch (ParseException ex) {
             JOptionPane.showMessageDialog(this,ex.getMessage());
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(this,ex.getMessage());
        }
    }//GEN-LAST:event_formComponentShown

