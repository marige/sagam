    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(testConnect()){
        File f= new File(System.getenv("APPDATA")+"\\compta");
        if(!f.exists())
        f.mkdir();
       // JOptionPane.showMessageDialog(this,System.getenv("APPDATA"));
        ObjectOutputStream oos = null;
        BddInfo obInfo= new BddInfo(txtUser.getText(),txtPasse.getText(),txtPort.getText(),txtBdd.getText(),txtIp.getText());
        try {
            oos = new ObjectOutputStream(new FileOutputStream(f+"\\cf.cmpt"));
            oos.writeObject(obInfo);
            oos.flush();
            new Controle();
            JOptionPane.showMessageDialog(this, "Enregistrement réussi");
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        finally{
            try {
                oos.close();

            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    private boolean testConnect(){
        boolean ok=false;
        try{
        com.mysql.jdbc.Driver driver;
        Connection cnt=null;

        try {
            driver = new com.mysql.jdbc.Driver();
            DriverManager.registerDriver(driver);
            cnt =DriverManager.getConnection("jdbc:mysql://"+txtIp.getText()+":"+txtPort.getText()+"/"+txtBdd.getText(),txtUser.getText(),txtPasse.getText());

            if(cnt!=null){
                ok=true;
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this,"Paramètres de connexion incorrecte");
        }
        finally{
            try {
                cnt.close();
            } catch (SQLException ex) {
              
            }

        }}catch(Exception ex){}
        return ok;
    }
